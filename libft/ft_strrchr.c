#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*result;

	i = -1;
	result = NULL;
	while (s[++i])
		if ((unsigned char)s[i] == c)
			result = ((char *)&s[i]);
	if ((unsigned char)s[i] == c)
		result = ((char *)&s[i]);
	return (result);
}
